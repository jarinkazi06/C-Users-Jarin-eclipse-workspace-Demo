package basic;

public class LearnEscapeSequence {

	public static void main(String[]arg) {
		System.out.println("This is Java basic");
		System.out.println("This is \tJava basic");
		System.out.println("This is Java basic\b1");
		System.out.println("This is Java \nbasic");
		System.out.println("This is \'Java\' basic");
		System.out.println("This is \"Java\" basic");
		System.out.println("This is Java \\basic");
		System.out.println("This is \rJava basic");
		System.out.println("This is \fJava basic");
	
	
	}
	
	
}
